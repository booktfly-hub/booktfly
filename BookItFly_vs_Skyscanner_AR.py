def sc(ws, row, col, value,
       font=None, fill=None, alignment=None, border=thin_border):
    cell = ws.cell(row=row, column=col, value=value)
    cell.font      = font      or body_font
    cell.alignment = alignment or rtl_wrap
    cell.border    = border
    if fill:
        cell.fill = fill
    return cell

def set_rtl(ws):
    ws.sheet_view.rightToLeft = True

